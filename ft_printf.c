int		main(void)
{
	va_list va;
	va_start (va, n);

	int i;

	for (i = 0; i < n; i++)
	{
		int c = va_arg (va, int);
		putchar (c);
	}
	putchar ('\n');
	va_end (va);

	return (0);
}
